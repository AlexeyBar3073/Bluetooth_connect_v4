/*
================================================================================
BASEOBJECT.H — БАЗОВЫЙ КЛАСС ДЛЯ АВТОНОМНЫХ ЗАДАЧ
================================================================================

НАЗНАЧЕНИЕ:
-----------
Абстрактный базовый класс для всех модулей системы, работающих в отдельных
задачах FreeRTOS. Предоставляет единый шаблон жизненного цикла и межзадачного
взаимодействия.

ВОЗМОЖНОСТИ:
------------
• Автоматическое создание задачи FreeRTOS при вызове start()
• Встроенная очередь команд для управления модулем
• Единый цикл задачи с хуками process() и onCommand()
• Управление состоянием (STOPPED / RUNNING / ERROR)
• Корректное завершение задачи при вызове stop()

КАК ИСПОЛЬЗОВАТЬ:
-----------------
1. Создать класс-наследник
2. Переопределить process() — бизнес-логика
3. Переопределить onCommand() — обработка команд (опционально)
4. Вызвать start() для запуска задачи

ПРИМЕР:
-------
class MyModule : public BaseObject {
public:
    MyModule() : BaseObject("MyModule") {}
protected:
    void process() override {
        // Выполняется в цикле задачи
    }
    void onCommand(Command cmd) override {
        if (cmd == Command::CMD_OTA_START) {
            // Реакция на команду
        }
    }
};

ВАЖНО:
------
• types.h должен быть подключён ПЕРЕД этим файлом!
• Не блокировать process() надолго — задачи должны отдавать управление
• Для ожидания использовать очереди/семафоры, а не циклы с delay()

================================================================================
*/

#ifndef BASE_OBJECT_H
#define BASE_OBJECT_H

#include "types.h"
#include <Arduino.h>
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"
#include "freertos/queue.h"

class BaseObject {
public:
    // ========================================================================
    // КОНСТРУКТОР / ДЕСТРУКТОР
    // ========================================================================
    
    /**
     * Создаёт объект и внутреннюю очередь команд.
     * Задача FreeRTOS НЕ запускается автоматически — нужен вызов start().
     */
    BaseObject(const char* name, 
               UBaseType_t priority = 5, 
               uint32_t stackSize = 4096, 
               BaseType_t core = 1);
    
    /// Виртуальный деструктор. Останавливает задачу и удаляет очередь команд.
    virtual ~BaseObject();

    // ========================================================================
    // УПРАВЛЕНИЕ ЖИЗНЕННЫМ ЦИКЛОМ
    // ========================================================================
    
    /**
     * Запускает задачу FreeRTOS.
     * Возвращает true если задача успешно создана, false при ошибке.
     */
    virtual bool start();
    
    /**
     * Останавливает и удаляет задачу FreeRTOS.
     * Очередь команд сохраняется, объект можно перезапустить.
     */
    virtual void stop();

    // ========================================================================
    // ИНФОРМАЦИЯ О СОСТОЯНИИ
    // ========================================================================
    
    bool isRunning() const;                     ///< true если задача активна
    ObjectState getState() const;               ///< Текущее состояние
    const char* getName() const;                ///< Имя задачи

    // ========================================================================
    // ОЧЕРЕДЬ КОМАНД
    // ========================================================================
    
    /// Возвращает хендл очереди команд (для внешнего использования).
    QueueHandle_t getCommandQueue() const { return _cmdQueue; }
    
    /// Отправляет команду во внутреннюю очередь (неблокирующая).
    bool sendCommand(Command cmd);

protected:
    // ========================================================================
    // ХУКИ ДЛЯ ПЕРЕОПРЕДЕЛЕНИЯ
    // ========================================================================
    
    /**
     * Основная бизнес-логика модуля.
     * Вызывается в бесконечном цикле задачи. ОБЯЗАТЕЛЬНА к переопределению.
     */
    virtual void process() = 0;
    
    /**
     * Обработчик команд из очереди.
     * Вызывается при получении команды. Переопределять не обязательно.
     */
    virtual void onCommand(Command cmd);

    // ========================================================================
    // ВНУТРЕННИЙ ЦИКЛ ЗАДАЧИ (НЕ ПЕРЕОПРЕДЕЛЯТЬ!)
    // ========================================================================
    
    void taskLoop();

    // ========================================================================
    // ЗАЩИЩЁННЫЕ ПОЛЯ
    // ========================================================================
    
    TaskHandle_t    _taskHandle;    ///< Хендл задачи FreeRTOS
    const char*     _name;          ///< Имя задачи (для логов)
    ObjectState     _state;         ///< Текущее состояние
    UBaseType_t     _priority;      ///< Приоритет задачи
    uint32_t        _stackSize;     ///< Размер стека в байтах
    BaseType_t      _core;          ///< Ядро (0 или 1)
    QueueHandle_t   _cmdQueue;      ///< Очередь команд (владеем)

private:
    // ========================================================================
    // СТАТИЧЕСКАЯ ОБЁРТКА ДЛЯ ЗАДАЧИ FREERTOS
    // ========================================================================
    static void taskWrapper(void* pvParameters);
};

#endif // BASE_OBJECT_H
/*
================================================================================
BASEOBJECT.H — БАЗОВЫЙ КЛАСС ДЛЯ АВТОНОМНЫХ ЗАДАЧ
================================================================================
НАЗНАЧЕНИЕ:
-----------
Абстрактный базовый класс для всех модулей системы, работающих в отдельных
задачах FreeRTOS. Предоставляет единый шаблон жизненного цикла и межзадачного
взаимодействия.
ВОЗМОЖНОСТИ:
------------
• Автоматическое создание задачи FreeRTOS в конструкторе
• Встроенная очередь команд для управления модулем
• Единый цикл задачи с хуками process() и onCommand()
• Управление состоянием (STOPPED / RUNNING / ERROR)
• Корректное завершение задачи при вызове destroy()
• Автоматическая подписка на системные команды через шину данных
КАК ИСПОЛЬЗОВАТЬ:
-----------------
1. Создать класс-наследник
2. Переопределить process() — бизнес-логика
3. Переопределить onSubscribe() — регистрация уникальных очередей в шине
4. Вызвать init(router) для подключения к шине и запуска обработки
ПРИМЕР:
-------
class MyModule : public BaseObject {
public:
    MyModule() : BaseObject("MyModule") {}
protected:
    void process() override {
        // Выполняется в цикле задачи
    }
    void onSubscribe() override {
        _router->registerQueue(Topic::MY_TOPIC, _myQueue);
    }
};
ВАЖНО:
------
• types.h должен быть подключён ПЕРЕД этим файлом!
• Не блокировать process() надолго — задачи должны отдавать управление
• Для ожидания использовать очереди/семафоры, а не циклы с delay()
• BaseObject не зависит от конкретной реализации роутера (IRouter)
================================================================================
*/
#ifndef BASE_OBJECT_H
#define BASE_OBJECT_H

#include "types.h"        
#include "IRouter.h"      
#include <Arduino.h>
#include "freertos/FreeRTOS.h"  
#include "freertos/task.h"
#include "freertos/queue.h"

class BaseObject {
public:
    // ========================================================================
    // КОНСТРУКТОР / ДЕСТРУКТОР
    // ========================================================================
    /**
     * Создаёт объект, очередь команд и задачу FreeRTOS.
     * Задача создаётся сразу, но переходит в режим ожидания до вызова init().
     */
    BaseObject(const char* name,
               UBaseType_t priority = 5,
               uint32_t stackSize = 4096,
               BaseType_t core = 1);

    /// Виртуальный деструктор. Останавливает задачу и удаляет очередь команд.
    virtual ~BaseObject();

    // ========================================================================
    // УПРАВЛЕНИЕ ЖИЗНЕННЫМ ЦИКЛОМ
    // ========================================================================
    /**
     * Подключает объект к шине данных, регистрирует подписки и активирует обработку.
     * Возвращает true при успешной инициализации.
     */
    virtual bool init(IRouter& router);

    /**
     * Отключает объект от шины, останавливает задачу и освобождает ресурсы.
     * После вызова объект можно удалить или повторно инициализировать.
     */
    virtual void destroy();

    // ========================================================================
    // ИНФОРМАЦИЯ О СОСТОЯНИИ
    // ========================================================================
    bool isReady() const;                     ///< true если объект инициализирован и работает
    ObjectState getState() const;             ///< Текущее состояние
    const char* getName() const;              ///< Имя задачи
    QueueHandle_t getCommandQueue() const { return _cmdQueue; } ///< Хендл очереди команд

protected:
    // ========================================================================
    // ХУКИ ДЛЯ ПЕРЕОПРЕДЕЛЕНИЯ
    // ========================================================================
    /**
     * Основная бизнес-логика модуля.
     * Вызывается в бесконечном цикле задачи. ОБЯЗАТЕЛЬНА к переопределению.
     */
    virtual void process() = 0;

    /**
     * Обработчик команд из очереди.
     * Вызывается при получении команды. Переопределять не обязательно.
     */
    virtual void onCommand(Command cmd);

    /**
     * Хук для регистрации специфичных очередей в шине данных.
     * Вызывается из init() ПОСЛЕ автоматической подписки на SYSTEM_CMD.
     */
    virtual void onSubscribe();

    IRouter*        _router = nullptr;        ///< Ссылка на абстрактную шину данных
    TaskHandle_t    _taskHandle = nullptr;    ///< Хендл задачи FreeRTOS
    const char*     _name;                    ///< Имя задачи (для логов)
    ObjectState     _state = ObjectState::STATE_STOPPED; ///< Текущее состояние
    bool            _initialized = false;     ///< Флаг готовности к обработке
    UBaseType_t     _priority;                ///< Приоритет задачи
    uint32_t        _stackSize;               ///< Размер стека в байтах
    BaseType_t      _core;                    ///< Ядро (0 или 1)
    QueueHandle_t   _cmdQueue = nullptr;      ///< Очередь команд (владеем)

private:
    void taskLoop();
    static void taskWrapper(void* pvParameters);
};

#endif // BASE_OBJECT_H
/*
================================================================================
BLUETOOTHMANAGER.H — МЕНЕДЖЕР BLUETOOTH SPP
================================================================================
НАЗНАЧЕНИЕ:
-----------
Управляет Bluetooth-соединением. Принимает входящие JSON-строки и отправляет
исходящие. Работает в отдельной задаче FreeRTOS.
ОСОБЕННОСТИ:
------------
• Принимает данные по SPP, разделённые символом '\n'
• Передаёт полученные строки в ProtocolManager через DataRouter
• Отправляет исходящие данные из очереди _outQueue
• Управляет LED для индикации состояния подключения
================================================================================
*/
#ifndef BLUETOOTH_MANAGER_H
#define BLUETOOTH_MANAGER_H

#include "types.h"
#include "BaseObject.h"
#include <BluetoothSerial.h>
#include "debug.h"

// ----------------------------------------------------------------------------
// КОНФИГУРАЦИЯ
// ----------------------------------------------------------------------------
#define BT_LED_PIN             2       // Пин для индикации
#define BT_DEFAULT_DEVICE_NAME "Car Monitor V3"
#define BT_LED_BLINK_INTERVAL  300     // Интервал мигания LED (мс)
#define BT_LED_BLINK_COUNT     5       // Количество миганий при отключении
#define BT_OUT_QUEUE_DEPTH     1       // Глубина очереди исходящих сообщений

class IRouter;

class BluetoothManager : public BaseObject {
public:
    // ========================================================================
    // КОНСТРУКТОР / ДЕСТРУКТОР
    // ========================================================================
    BluetoothManager(const char* name = "BT_Manager",
                     UBaseType_t priority = 2,
                     uint32_t stackSize = 4096,
                     BaseType_t core = 0);
    ~BluetoothManager();

    // ========================================================================
    // ИНИЦИАЛИЗАЦИЯ И УПРАВЛЕНИЕ
    // ========================================================================
    /// Инициализирует Bluetooth и подключает к шине данных.
    bool init(IRouter& router, const char* deviceName = BT_DEFAULT_DEVICE_NAME);
    /// Останавливает задачу и освобождает ресурсы.
    void destroy() override;

    // ========================================================================
    // ДОСТУП К ОЧЕРЕДЯМ (для прямых связей)
    // ========================================================================
    /// Возвращает очередь для отправки данных в Bluetooth.
    QueueHandle_t getOutQueue() const { return _outQueue; }

    // ========================================================================
    // ОТПРАВКА ДАННЫХ
    // ========================================================================
    /// Отправляет JSON-строку в ProtocolManager через шину данных.
    void sendToProtocol(const char* json);

    // ========================================================================
    // СОСТОЯНИЕ
    // ========================================================================
    bool isConnected() const { return _isConnected; }

protected:
    // ========================================================================
    // ХУКИ БАЗОВОГО КЛАССА
    // ========================================================================
    void process() override;
    void onCommand(Command cmd) override;
    void onSubscribe() override;

private:
    // ========================================================================
    // АППАРАТНЫЕ РЕСУРСЫ
    // ========================================================================
    BluetoothSerial _bt;                // Объект Bluetooth SPP

    // ========================================================================
    // ОЧЕРЕДИ
    // ========================================================================
    QueueHandle_t _outQueue;            // Очередь исходящих сообщений (владеем)

    // ========================================================================
    // СОСТОЯНИЕ ПОДКЛЮЧЕНИЯ
    // ========================================================================
    bool _isConnected;                  // true = клиент подключен

    // ========================================================================
    // УПРАВЛЕНИЕ LED
    // ========================================================================
    uint32_t _lastBlinkTime;            // Время последнего переключения LED
    uint8_t  _blinkCount;               // Счётчик миганий
    bool     _ledState;                 // Текущее состояние LED
    bool     _blinkSequenceActive;      // Активна ли серия миганий
    void _updateLed();                  // Обновляет состояние LED
    void _handleConnectionChange(bool currentState);  // Обработка подключения/отключения
    void _setLed(bool state);           // Устанавливает LED в заданное состояние

    // ========================================================================
    // ОБРАБОТКА ДАННЫХ
    // ========================================================================
    void _processTx();                  // Отправка данных из очереди в Bluetooth
    void _processRx();                  // Чтение данных из Bluetooth и передача в Protocol
};

#endif // BLUETOOTH_MANAGER_H
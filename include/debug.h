/*
================================================================================
DEBUG MACROS — ЦЕНТРАЛИЗОВАННОЕ УПРАВЛЕНИЕ ОТЛАДКОЙ
================================================================================
Файл: debug.h
Назначение:
-----------
Единая точка управления отладочным выводом для всего проекта.
Позволяет полностью исключить код логирования из релизной сборки,
экономя Flash-память и избегая накладных расходов.
Принципы работы:
----------------
• В режиме DEBUG (DEBUG_LOG=1):
- Логи выводятся в Serial
- Форматированный вывод использует СТЕКОВЫЙ буфер (не фрагментирует кучу)
• В режиме RELEASE (DEBUG_LOG=0):
- ВСЕ макросы раскрываются в do{}while(0)
- Компилятор полностью удаляет код логирования
- Ни байта строк, ни вызовов функций не попадает в прошивку
Использование:
--------------
• DBG_PRINT("text") — простой вывод
• DBG_PRINTF("val=%d", x) — форматированный вывод (только в Debug!)
• DBG_PRINTLN("") — вывод с переносом строки
Важно:
--------
• Не добавлять '\n' в аргументы макросов — они добавляют его сами
• В Release-сборке выражения внутри макросов НЕ вычисляются (экономия CPU)
Автор: [Ваше имя/команда]
Версия: 3.0
Дата: 2026-04-22
================================================================================
*/
#ifndef DEBUG_H
#define DEBUG_H

#include <Arduino.h>

// ============================================================================
// КОНФИГУРАЦИЯ: установить DEBUG_LOG=1 в platformio.ini для отладки
// ============================================================================
// Пример в platformio.ini:
// build_flags =
//     -D DEBUG_LOG=1   ; для Debug-сборки
//     -D DEBUG_LOG=0   ; для Release-сборки (по умолчанию)
// ============================================================================
// МАКРОСЫ ОТЛАДКИ
// ============================================================================
#if DEBUG_LOG
// ------------------------------------------------------------------------
// Debug-сборка: логи активны
// ------------------------------------------------------------------------
/// Простой вывод строки
#define DBG_PRINT(msg)      Serial.print(msg)
/// Вывод строки с переносом
#define DBG_PRINTLN(msg)    Serial.println(msg)
/// Пустая строка (для разделения блоков лога)
#define DBG_NEWLINE()       Serial.println()
/**
 * @brief Форматированный вывод (аналог printf)
 *
 * @details
 * Использует буфер на СТЕКЕ (64 байта), чтобы не фрагментировать кучу.
 * Автоматически добавляет '\n' в конце.
 *
 * Пример: DBG_PRINTF("Heap: %u", ESP.getFreeHeap());
 *
 * @warning Не использовать для строк > 64 символов — обрежется!
 */
#define DBG_PRINTF(fmt, ...) do { \
    char _dbg_buf[96]; \
    snprintf(_dbg_buf, sizeof(_dbg_buf), fmt, ##__VA_ARGS__); \
    Serial.println(_dbg_buf); \
} while(0)
#else
// ------------------------------------------------------------------------
// Release-сборка: ВСЕ логи исчезают на этапе препроцессора
// ------------------------------------------------------------------------
#define DBG_PRINT(msg)      do {} while(0)
#define DBG_PRINTLN(msg)    do {} while(0)
#define DBG_NEWLINE()       do {} while(0)
#define DBG_PRINTF(fmt, ...) do {} while(0)
#endif // DEBUG_LOG

#endif // DEBUG_H
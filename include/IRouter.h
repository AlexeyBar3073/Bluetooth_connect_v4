#ifndef I_ROUTER_H
#define I_ROUTER_H

#include "types.h"
#include "freertos/FreeRTOS.h"
#include "freertos/queue.h"

class IRouter {
public:
    virtual ~IRouter() = default;

    /**
     * @brief Регистрация очереди для получения сообщений топика.
     * @param topic Идентификатор топика
     * @param queue Хендл очереди FreeRTOS
     */
    virtual void registerQueue(Topic topic, QueueHandle_t queue) = 0;

    /**
     * @brief Публикация данных в топик.
     * @param topic Идентификатор топика
     * @param data Указатель на данные
     * @return true при успешной постановке в очередь
     */
    virtual bool publish(Topic topic, const void* data) = 0;
};

#endif // I_ROUTER_H
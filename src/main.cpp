#include <Arduino.h>
#include "BluetoothManager.h"

// Создание глобального экземпляра
BluetoothManager bt;

void setup() {
    Serial.begin(115200);
    delay(1000);
    Serial.println("\n[System] Boot V3 (OOP Autonomous Architecture)");

    // Запуск объекта
    // После этого он живет своей жизнью в отдельной задаче
    if (!bt.start("Car Monitor")) {
        Serial.println("[System] Critical: BT Start Failed");
        while(true);
    }
}

void loop() {
    // Здесь нет вызовов bt.update() или bt.handle()
    // Объект работает автономно.
    
    // Пример: если нужно что-то отправить, просто вызываем метод
    if (bt.isConnected()) {
        // bt.write(...); 
    }

    // Остальная логика системы
    delay(100);
}